            ("4a8b5280-fe6f-11ec-a30b-d4619d029786", "国家安全", "https://www.hoover.org/research-teams/national-security-task-force", "政治"),
            ("9d11275e-f093-307a-9b36-eb56284debc4", "外交与国家安全", "", "政治"),
