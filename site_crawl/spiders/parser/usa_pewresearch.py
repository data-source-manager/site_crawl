            ("d219e49f-3b1f-3dc8-99e9-2d7d9933bd0a", "地区和国家/亚太地区", "https://www.pewresearch.org/regions-countries/asia-pacific/", "政治"),
            ("72a42c86-bf08-3eef-8f14-c057991b8582", "所有出版物", "https://www.pewresearch.org/topic/politics-policy/", "政治"),
